-- Consultations Policies
CREATE POLICY "Super admins can manage all consultations" ON public.consultations
  FOR ALL USING (
    EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND role = 'super_admin')
  );

CREATE POLICY "Group admins can manage group consultations" ON public.consultations
  FOR ALL USING (
    EXISTS (
      SELECT 1 FROM public.profiles p
      JOIN public.projects proj ON proj.id = public.consultations.project_id
      WHERE p.id = auth.uid() AND p.role = 'group_admin' AND p.group_id = proj.group_id
    )
  );

CREATE POLICY "Experts can view/update assigned consultations" ON public.consultations
  FOR ALL USING (auth.uid() = expert_id);

CREATE POLICY "Companies can view their own consultations" ON public.consultations
  FOR SELECT USING (auth.uid() = company_id);

